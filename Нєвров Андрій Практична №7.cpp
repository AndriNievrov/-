#include <stdio.h>
#include <math.h>

// Функція для обчислення кількості точок перетину двох кіл
int findIntersectionPoints(int x1, int y1, int r1, int x2, int y2, int r2) {
    // Відстань між центрами кіл
    double d = sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));

    // Перевірка випадків, коли кількість точок перетину:
    // -1: безкінечно багато точок перетину (співпадають)
    if (d == 0 && r1 == r2) {
        return -1;
    }
    // 0: не перетинаються (відстань більша за суму радіусів або одна всередині іншої без дотику)
    if (d > r1 + r2 || d < fabs(r1 - r2)) {
        return 0;
    }
    // 1: дотикаються зовні або всередині
    if (d == r1 + r2 || d == fabs(r1 - r2)) {
        return 1;
    }
    // 2: мають дві точки перетину
    return 2;
}

int main() {
    int x1, y1, r1, x2, y2, r2;

    // Введення координат центрів і радіусів двох кіл
    printf("Введіть координати центру та радіус першого кола (x1, y1, r1): ");
    scanf("%d %d %d", &x1, &y1, &r1);
    printf("Введіть координати центру та радіус другого кола (x2, y2, r2): ");
    scanf("%d %d %d", &x2, &y2, &r2);

    // Обчислення та виведення кількості точок перетину
    int result = findIntersectionPoints(x1, y1, r1, x2, y2, r2);
    printf("Кількість точок перетину: %d\n", result);

    return 0;
}

